function renderRows(items, renderer) {
  finderRows.innerHTML = "";
  if (!items.length) {
    const empty = document.createElement("p");
    empty.className = "empty-folder";
    empty.textContent = "Bu klasör boş.";
    finderRows.appendChild(empty);
    return;
  }

  const columns = getColumnsPerRow();
  const rows = [];
  let currentRow = [];
  let currentWidth = 0;

  items.forEach((item, index) => {
    const itemWidth = getItemSpan(item);
    if (itemWidth >= columns) {
      if (currentRow.length) {
        rows.push(currentRow);
        currentRow = [];
        currentWidth = 0;
      }
      rows.push([{ item, index }]);
      return;
    }

    if (currentWidth + itemWidth > columns && currentRow.length) {
      rows.push(currentRow);
      currentRow = [];
      currentWidth = 0;
    }

    currentRow.push({ item, index });
    currentWidth += itemWidth;
  });

  if (currentRow.length) rows.push(currentRow);

  rows.forEach((rowItems) => {
    const row = document.createElement("div");
    row.className = "finder-row";
    rowItems.forEach(({ item, index }) => row.appendChild(renderer(item, index)));
    finderRows.appendChild(row);
  });
}

function getItemSpan(item) {
  if (item.size === "full") return getColumnsPerRow();
  if (item.size === "large") return 2;
  return 1;
}

function getItemSizeClass(item) {
  if (item.size === "full") return "is-size-full";
  if (item.size === "large") return "is-size-large";
  if (item.size === "small") return "is-size-small";
  return "is-size-medium";
}

